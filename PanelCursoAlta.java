    private void butAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butAceptarActionPerformed
        if(camposVacios()){
            JOptionPane.showMessageDialog(this,
                    "Hay campos obligatorios vacios!!",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
        try{
            Curso c = new Curso();
            c.setId(Integer.valueOf(txtID.getText()));//mamnananananan//
            c.setTitulo(txtTitulo.getText());
            if(!txtHoras.getText().equals(""))
                c.setHoras(Double.valueOf(txtHoras.getText()));
            c.altaCurso(bd);
            JOptionPane.showMessageDialog(this,
                    "Alta de curso correcta.ID: "+c.getId(),
                    "Alta curso",
                    JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,
                    "Error!! "+e.getMessage(),"Error",
                    JOptionPane.ERROR_MESSAGE);
        }
        setVisible(false);
        
    }//GEN-LAST:event_butAceptarActionPerformed

